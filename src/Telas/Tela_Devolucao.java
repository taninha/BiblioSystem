    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Devolucao d = new Devolucao();
        DevolucaoIn in = new DevolucaoIn();
            
            
            d.setTitulo_livro(jConsultaLivro.getValueAt(jConsultaLivro.getSelectedRow(),0).toString());
            d.setNome_usuario(jConsultaLivro.getValueAt(jConsultaLivro.getSelectedRow(),1).toString());
            d.setCpf_usuario(jConsultaLivro.getValueAt(jConsultaLivro.getSelectedRow(),2).toString());
            d.setData_emprestimo(jDataemp.getText());
            d.setData_devolucao(jDatadev.getText());
            d.setData_entregue(jDataen.getText());
            d.setMulta_usuario(Double.parseDouble(jMulta.getText()));
        
            
                in.create(d);
                dispose();      
    }//GEN-LAST:event_btCadastrarActionPerformed

