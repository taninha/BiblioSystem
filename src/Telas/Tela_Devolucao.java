    public void readJTableLivro(){
     DefaultTableModel modelo = (DefaultTableModel) jConsultaLivro.getModel();
     modelo.setNumRows(0);
        DevolucaoIn DIn = new DevolucaoIn();
        
        for(Reserva l: DIn.read()){
            modelo.addRow(new Object[]{
                l.getTítulo_livro(),
                l.getNome_usuario(),
                l.getCpf_usuario(),
                l.getData_emprestimo(),
            });
        }
    }
    
        public void readJTableForDesc(String desc){
     DefaultTableModel modelo = (DefaultTableModel) jConsultaLivro.getModel();
     modelo.setNumRows(0);
        DevolucaoIn DIn = new DevolucaoIn();
        
        for(Reserva l: DIn.readForDesc(desc)){
            modelo.addRow(new Object[]{
                l.getTítulo_livro(),
                l.getNome_usuario(),
                l.getCpf_usuario(),
                l.getData_emprestimo(),
            });
        }
    }
    


    

