    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        if(jConsulta.getSelectedRow() != -1){
        
        Livro c = new Livro();
        LivroIn in = new LivroIn();
        
       c.setId_livro((int)jConsulta.getValueAt(jConsulta.getSelectedRow(),0));
       c.setTitulo_livro(titulo.getText());
       c.setAutor_livro(autor.getText());
       c.setGenero_livro(genero.getText());
       c.setAnoPubli_livro(anoPub.getText());
       c.setEditora_livro(editora.getText());
       c.setNumeroPag_livro(numPag.getText());
       c.setIsbn_livro(isbn.getText());
       c.setCodBarras_livro(codBarra.getText());
        
        in.update(c);
        
        readJTable();
        }
    }//GEN-LAST:event_SalvarActionPerformed

