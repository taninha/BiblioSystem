    public void readJTable(){
     DefaultTableModel modelo = (DefaultTableModel) jConsulta.getModel();
     modelo.setNumRows(0);
        LivroIn LIn = new LivroIn();
        
        for(Livro l: LIn.read()){
            modelo.addRow(new Object[]{
                l.getId_livro(),
                l.getTitulo_livro(),
                l.getAutor_livro(),
                l.getGenero_livro(),
                l.getAnoPubli_livro(),
                l.getEditora_livro(),
                l.getNumeroPag_livro(),
                l.getIsbn_livro(),
                l.getCodBarras_livro(),
                
        });
    }
    }
    
    public void readJTableForDesc(String desc){
     DefaultTableModel modelo = (DefaultTableModel) jConsulta.getModel();
     modelo.setNumRows(0);
        LivroIn LIn = new LivroIn();
        
        for(Livro l: LIn.readForDesc(desc)){
            modelo.addRow(new Object[]{
                l.getId_livro(),
                l.getTitulo_livro(),
                l.getAutor_livro(),
                l.getGenero_livro(),
                l.getAnoPubli_livro(),
                l.getEditora_livro(),
                l.getNumeroPag_livro(),
                l.getIsbn_livro(),
                l.getCodBarras_livro(),
                
        });
    }
    }
                    

